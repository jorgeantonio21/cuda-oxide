/// Same as `run_command`, but injects extra `(key, value)` pairs into the
/// child's environment first. Used to forward the discovered `MATHDX_ROOT`
/// to `extern-libs/build.sh` even when the parent shell didn't export it.
fn run_command_with_env(
    cmd: &str,
    args: &[&str],
    cwd: &Path,
    extra_env: &[(&str, &str)],
) -> Result<(), String> {
    let mut command = Command::new(cmd);
    command.args(args).current_dir(cwd);
    for (k, v) in extra_env {
        command.env(k, v);
    }
    let status = command
        .status()
        .map_err(|e| format!("Failed to run {}: {}", cmd, e))?;

    if status.success() {
        Ok(())
    } else {
        Err(format!("{} failed with exit code {:?}", cmd, status.code()))
    }
}

/// Checks if a target file needs to be rebuilt based on source file timestamps.
fn file_needs_rebuild(target: &Path, sources: &[&Path]) -> bool {
    if !target.exists() {
        return true;
    }
    let target_time = target.metadata().and_then(|m| m.modified()).ok();
    for src in sources {
        if let Ok(src_time) = src.metadata().and_then(|m| m.modified())
            && target_time.map(|t| src_time > t).unwrap_or(true)
        {
            return true;
        }
    }
    false
}

/// Builds the LTOIR tools (compile_ltoir, link_ltoir) if they don't exist.
fn build_tools(example_dir: &Path) -> Result<(), String> {
    let tools_dir = example_dir.join("tools");
    let compile_ltoir = tools_dir.join("compile_ltoir");
    let link_ltoir = tools_dir.join("link_ltoir");

    if compile_ltoir.exists() && link_ltoir.exists() {
        return Ok(());
    }

    println!("=== Building LTOIR tools ===");
    run_command("./build_tools.sh", &[], &tools_dir)?;
    println!("  ✓ Tools built\n");
    Ok(())
}

/// Builds external MathDx CUDA C++ files to LTOIR.
/// Always does a clean rebuild during development.
fn build_external_ltoir(example_dir: &Path, mathdx_root: &Path) -> Result<(), String> {
    let extern_libs_dir = example_dir.join("extern-libs");

    println!("=== Building MathDx LTOIR ===");
    println!("    Using MATHDX_ROOT={}", mathdx_root.display());
    let mathdx_root_str = mathdx_root
        .to_str()
        .ok_or_else(|| format!("MATHDX_ROOT is not valid UTF-8: {}", mathdx_root.display()))?;
    // Always clean rebuild during development to ensure fresh LTOIR
    run_command_with_env(
        "./build.sh",
        &["--clean", arch()],
        &extern_libs_dir,
        &[("MATHDX_ROOT", mathdx_root_str)],
    )?;
    println!("  ✓ MathDx LTOIR built\n");
    Ok(())
}

/// Compiles cuda-oxide LLVM IR (.ll) to LTOIR using libNVVM.
fn compile_cuda_oxide_ltoir(example_dir: &Path) -> Result<(), String> {
    let ll_file = example_dir.join("mathdx_ffi_test.ll");
    let ltoir_file = example_dir.join("mathdx_ffi_test.ltoir");
    let tools_dir = example_dir.join("tools");

    if !ll_file.exists() {
        return Err(format!(
            "cuda-oxide LLVM IR not found: {}\n\
             Run: cargo oxide run mathdx_ffi_test --emit-nvvm-ir --arch={}",
            ll_file.display(),
            arch()
        ));
    }

    if !file_needs_rebuild(&ltoir_file, &[&ll_file]) {
        return Ok(());
    }

    println!("=== Compiling cuda-oxide LLVM IR to LTOIR ===");
    run_command(
        "./compile_ltoir",
        &[
            ll_file.to_str().unwrap(),
            arch(),
            ltoir_file.to_str().unwrap(),
        ],
        &tools_dir,
    )?;
    println!("  ✓ cuda-oxide LTOIR compiled\n");
    Ok(())
}

/// Links all LTOIR files into a single cubin using nvJitLink.
/// Includes: cuda-oxide, MathDx C++ wrappers, and optionally libmathdx-generated LTOIR.
fn link_ltoir(
    example_dir: &Path,
    libmathdx_ltoirs: &[std::path::PathBuf],
) -> Result<std::path::PathBuf, String> {
    let tools_dir = example_dir.join("tools");
    let extern_libs_dir = example_dir.join("extern-libs");
    let cubin_file = example_dir.join("merged.cubin");
    let cuda_oxide_ltoir = example_dir.join("mathdx_ffi_test.ltoir");
    let cufftdx_ltoir = extern_libs_dir.join("cufftdx_wrappers.ltoir");
    let cublasdx_ltoir = extern_libs_dir.join("cublasdx_wrappers.ltoir");

    // Build list of LTOIR sources for dependency checking
    let mut sources: Vec<&Path> = vec![
        cuda_oxide_ltoir.as_path(),
        cufftdx_ltoir.as_path(),
        cublasdx_ltoir.as_path(),
    ];
    for ltoir in libmathdx_ltoirs {
        sources.push(ltoir.as_path());
    }

    if !file_needs_rebuild(&cubin_file, &sources) {
        return Ok(cubin_file);
    }

    println!("=== Linking LTOIR files ===");
    println!("    Sources:");
    println!("      - cuda-oxide: {}", cuda_oxide_ltoir.display());
    println!("      - cuFFTDx wrappers: {}", cufftdx_ltoir.display());
    println!("      - cuBLASDx wrappers: {}", cublasdx_ltoir.display());
    for ltoir in libmathdx_ltoirs {
        println!("      - libmathdx generated: {}", ltoir.display());
    }

    // Build command arguments - collect strings to extend lifetimes
    let arch_arg = format!("-arch={}", arch());
    let cubin_str = cubin_file.to_str().unwrap().to_string();
    let cuda_oxide_str = cuda_oxide_ltoir.to_str().unwrap().to_string();
    let cufftdx_str = cufftdx_ltoir.to_str().unwrap().to_string();
    let cublasdx_str = cublasdx_ltoir.to_str().unwrap().to_string();
    let libmathdx_strs: Vec<String> = libmathdx_ltoirs
        .iter()
        .map(|p| p.to_str().unwrap().to_string())
        .collect();

    let mut args: Vec<&str> = vec![
        &arch_arg,
        "-o",
        &cubin_str,
        &cuda_oxide_str,
        &cufftdx_str,
        &cublasdx_str,
    ];
    for s in &libmathdx_strs {
        args.push(s);
    }

    run_command("./link_ltoir", &args, &tools_dir)?;
    println!("  ✓ LTOIR linked to cubin\n");
    Ok(cubin_file)
}

/// Runs the complete build pipeline.
fn build_pipeline(mathdx_root: &Path) -> Result<std::path::PathBuf, String> {
    let example_dir = get_example_dir();

    build_tools(&example_dir)?;
    build_external_ltoir(&example_dir, mathdx_root)?;

    // Generate LTOIR using libmathdx C API (Option B) - only if feature enabled
    #[cfg(feature = "libmathdx")]
    let libmathdx_ltoirs = generate_libmathdx_ltoir(&example_dir)?;
    #[cfg(not(feature = "libmathdx"))]
    let libmathdx_ltoirs: Vec<std::path::PathBuf> = Vec::new();

    compile_cuda_oxide_ltoir(&example_dir)?;
    link_ltoir(&example_dir, &libmathdx_ltoirs)
}

