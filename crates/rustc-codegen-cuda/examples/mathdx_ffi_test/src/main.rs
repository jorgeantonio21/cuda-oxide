// =============================================================================
// Build Pipeline
//
// These functions automate the LTOIR build process:
// 1. Build C tools (compile_ltoir, link_ltoir) if not present
// 2. Build external CUDA C++ to LTOIR if sources changed
// 3. Generate libmathdx LTOIR (Option B - no C++ wrappers needed)
// 4. Compile cuda-oxide LLVM IR to LTOIR
// 5. Link all LTOIR files into a cubin
// =============================================================================

use std::path::Path;
use std::process::Command;

// =============================================================================
// libmathdx LTOIR Generation (Option B)
//
// This module demonstrates using libmathdx-sys to generate LTOIR at build time
// without writing any C++ wrapper code. The key steps are:
// 1. Create a descriptor for the operation (FFT, GEMM, etc.)
// 2. Set operators (size, precision, direction, SM, etc.)
// 3. Set a custom symbol name via COMMONDX_OPTION_SYMBOL_NAME
// 4. Finalize and extract LTOIR bytes
//
// Only available when the "libmathdx" feature is enabled.
// =============================================================================

/// Generates LTOIR for FFT operations using libmathdx C API.
/// Returns the paths to the generated LTOIR files.
#[cfg(feature = "libmathdx")]
fn generate_libmathdx_ltoir(example_dir: &Path) -> Result<Vec<std::path::PathBuf>, String> {
    use libmathdx_sys::*;

    let fwd_path = example_dir.join("libmathdx_fft_fwd.ltoir");
    let inv_path = example_dir.join("libmathdx_fft_inv.ltoir");

    // Skip if LTOIR already exists (for faster iteration)
    // Remove this check to always regenerate
    if fwd_path.exists() && inv_path.exists() {
        println!("  (using cached libmathdx LTOIR)");
        return Ok(vec![fwd_path, inv_path]);
    }

    println!("=== Generating libmathdx LTOIR ===");
    println!("    Using libmathdx C API (no C++ wrappers needed)");

    // Extract SM number from arch() (e.g., "sm_120" -> 1200)
    let arch_str = arch();
    let sm_num: i64 = arch_str
        .strip_prefix("sm_")
        .and_then(|s| s.parse::<i64>().ok())
        .map(|n| n * 10) // sm_120 -> 1200
        .ok_or_else(|| format!("Invalid ARCH format: {}", arch_str))?;

    unsafe {
        // Create a code handle to collect all generated LTOIR
        let mut code: commondxCode = 0;
        let status = commondxCreateCode(&mut code);
        if status != commondxStatusType_t_COMMONDX_SUCCESS {
            return Err(format!("Failed to create code handle: {:?}", status));
        }

        // Set target SM for code generation
        commondxSetCodeOptionInt64(code, commondxOption_t_COMMONDX_OPTION_TARGET_SM, sm_num);

        // Set code container to LTOIR
        commondxSetCodeOptionInt64(
            code,
            commondxOption_t_COMMONDX_OPTION_CODE_CONTAINER,
            commondxCodeContainer_t_COMMONDX_CODE_CONTAINER_LTOIR as i64,
        );

        // Generate Forward FFT descriptor
        let mut fft_fwd: cufftdxDescriptor = 0;
        cufftdxCreateDescriptor(&mut fft_fwd);

        // Configure 16-point C2C forward FFT
        cufftdxSetOperatorInt64(fft_fwd, cufftdxOperatorType_t_CUFFTDX_OPERATOR_SIZE, 16);
        cufftdxSetOperatorInt64(
            fft_fwd,
            cufftdxOperatorType_t_CUFFTDX_OPERATOR_TYPE,
            cufftdxType_t_CUFFTDX_TYPE_C2C as i64,
        );
        cufftdxSetOperatorInt64(
            fft_fwd,
            cufftdxOperatorType_t_CUFFTDX_OPERATOR_DIRECTION,
            cufftdxDirection_t_CUFFTDX_DIRECTION_FORWARD as i64,
        );
        cufftdxSetOperatorInt64(
            fft_fwd,
            cufftdxOperatorType_t_CUFFTDX_OPERATOR_PRECISION,
            commondxPrecision_t_COMMONDX_PRECISION_F32 as i64,
        );
        cufftdxSetOperatorInt64(fft_fwd, cufftdxOperatorType_t_CUFFTDX_OPERATOR_SM, sm_num);
        cufftdxSetOperatorInt64(
            fft_fwd,
            cufftdxOperatorType_t_CUFFTDX_OPERATOR_EXECUTION,
            commondxExecution_t_COMMONDX_EXECUTION_THREAD as i64,
        );
        cufftdxSetOperatorInt64(
            fft_fwd,
            cufftdxOperatorType_t_CUFFTDX_OPERATOR_API,
            cufftdxApi_t_CUFFTDX_API_LMEM as i64,
        );

        // Set custom symbol name (Option B key step!)
        let fwd_name = CString::new("libmathdx_fft_16_forward").unwrap();
        cufftdxSetOptionStr(
            fft_fwd,
            commondxOption_t_COMMONDX_OPTION_SYMBOL_NAME,
            fwd_name.as_ptr(),
        );

        // Generate Inverse FFT descriptor
        let mut fft_inv: cufftdxDescriptor = 0;
        cufftdxCreateDescriptor(&mut fft_inv);

        // Configure 16-point C2C inverse FFT (same as forward, just different direction)
        cufftdxSetOperatorInt64(fft_inv, cufftdxOperatorType_t_CUFFTDX_OPERATOR_SIZE, 16);
        cufftdxSetOperatorInt64(
            fft_inv,
            cufftdxOperatorType_t_CUFFTDX_OPERATOR_TYPE,
            cufftdxType_t_CUFFTDX_TYPE_C2C as i64,
        );
        cufftdxSetOperatorInt64(
            fft_inv,
            cufftdxOperatorType_t_CUFFTDX_OPERATOR_DIRECTION,
            cufftdxDirection_t_CUFFTDX_DIRECTION_INVERSE as i64,
        );
        cufftdxSetOperatorInt64(
            fft_inv,
            cufftdxOperatorType_t_CUFFTDX_OPERATOR_PRECISION,
            commondxPrecision_t_COMMONDX_PRECISION_F32 as i64,
        );
        cufftdxSetOperatorInt64(fft_inv, cufftdxOperatorType_t_CUFFTDX_OPERATOR_SM, sm_num);
        cufftdxSetOperatorInt64(
            fft_inv,
            cufftdxOperatorType_t_CUFFTDX_OPERATOR_EXECUTION,
            commondxExecution_t_COMMONDX_EXECUTION_THREAD as i64,
        );
        cufftdxSetOperatorInt64(
            fft_inv,
            cufftdxOperatorType_t_CUFFTDX_OPERATOR_API,
            cufftdxApi_t_CUFFTDX_API_LMEM as i64,
        );

        // Set custom symbol name for inverse
        let inv_name = CString::new("libmathdx_fft_16_inverse").unwrap();
        cufftdxSetOptionStr(
            fft_inv,
            commondxOption_t_COMMONDX_OPTION_SYMBOL_NAME,
            inv_name.as_ptr(),
        );

        // Finalize forward FFT descriptor into the code handle
        let status = cufftdxFinalizeCode(code, fft_fwd);
        if status != commondxStatusType_t_COMMONDX_SUCCESS {
            cufftdxDestroyDescriptor(fft_fwd);
            cufftdxDestroyDescriptor(fft_inv);
            commondxDestroyCode(code);
            return Err(format!("Failed to finalize forward FFT: {:?}", status));
        }
        cufftdxDestroyDescriptor(fft_fwd);

        // Create a second code handle for inverse FFT
        // (libmathdx may not support multiple descriptors in one code handle)
        let mut code_inv: commondxCode = 0;
        let status = commondxCreateCode(&mut code_inv);
        if status != commondxStatusType_t_COMMONDX_SUCCESS {
            cufftdxDestroyDescriptor(fft_inv);
            commondxDestroyCode(code);
            return Err(format!(
                "Failed to create inverse code handle: {:?}",
                status
            ));
        }
        commondxSetCodeOptionInt64(code_inv, commondxOption_t_COMMONDX_OPTION_TARGET_SM, sm_num);
        commondxSetCodeOptionInt64(
            code_inv,
            commondxOption_t_COMMONDX_OPTION_CODE_CONTAINER,
            commondxCodeContainer_t_COMMONDX_CODE_CONTAINER_LTOIR as i64,
        );

        let status = cufftdxFinalizeCode(code_inv, fft_inv);
        if status != commondxStatusType_t_COMMONDX_SUCCESS {
            cufftdxDestroyDescriptor(fft_inv);
            commondxDestroyCode(code);
            commondxDestroyCode(code_inv);
            return Err(format!("Failed to finalize inverse FFT: {:?}", status));
        }
        cufftdxDestroyDescriptor(fft_inv);

        // Get LTOIR from forward FFT code handle
        let mut ltoir_fwd_size: usize = 0;
        let status = commondxGetCodeLTOIRSize(code, &mut ltoir_fwd_size);
        if status != commondxStatusType_t_COMMONDX_SUCCESS {
            commondxDestroyCode(code);
            commondxDestroyCode(code_inv);
            return Err(format!("Failed to get forward LTOIR size: {:?}", status));
        }

        let mut ltoir_fwd_data = vec![0u8; ltoir_fwd_size];
        let status = commondxGetCodeLTOIR(
            code,
            ltoir_fwd_size,
            ltoir_fwd_data.as_mut_ptr() as *mut std::ffi::c_void,
        );
        if status != commondxStatusType_t_COMMONDX_SUCCESS {
            commondxDestroyCode(code);
            commondxDestroyCode(code_inv);
            return Err(format!("Failed to get forward LTOIR: {:?}", status));
        }

        // Get LTOIR from inverse FFT code handle
        let mut ltoir_inv_size: usize = 0;
        let status = commondxGetCodeLTOIRSize(code_inv, &mut ltoir_inv_size);
        if status != commondxStatusType_t_COMMONDX_SUCCESS {
            commondxDestroyCode(code);
            commondxDestroyCode(code_inv);
            return Err(format!("Failed to get inverse LTOIR size: {:?}", status));
        }

        let mut ltoir_inv_data = vec![0u8; ltoir_inv_size];
        let status = commondxGetCodeLTOIR(
            code_inv,
            ltoir_inv_size,
            ltoir_inv_data.as_mut_ptr() as *mut std::ffi::c_void,
        );
        if status != commondxStatusType_t_COMMONDX_SUCCESS {
            commondxDestroyCode(code);
            commondxDestroyCode(code_inv);
            return Err(format!("Failed to get inverse LTOIR: {:?}", status));
        }

        // Write both LTOIRs to separate files
        std::fs::write(&fwd_path, &ltoir_fwd_data)
            .map_err(|e| format!("Failed to write forward LTOIR: {}", e))?;
        std::fs::write(&inv_path, &ltoir_inv_data)
            .map_err(|e| format!("Failed to write inverse LTOIR: {}", e))?;

        println!(
            "    Generated {} bytes (forward) + {} bytes (inverse)",
            ltoir_fwd_size, ltoir_inv_size
        );
        println!("    Symbols: libmathdx_fft_16_forward, libmathdx_fft_16_inverse");
        println!("  ✓ libmathdx LTOIR generated\n");

        // Cleanup
        commondxDestroyCode(code);
        commondxDestroyCode(code_inv);
    }

    Ok(vec![fwd_path, inv_path])
}

/// Target GPU architecture for LTOIR compilation and linking.
///
/// Reads `CUDA_OXIDE_TARGET` (set by `cargo oxide run --arch=...`), falling
/// back to `sm_120`. Leaking the string keeps a `&'static str` interface
/// matching the rest of the example.
fn arch() -> &'static str {
    if let Ok(target) = std::env::var("CUDA_OXIDE_TARGET") {
        return Box::leak(target.into_boxed_str());
    }
    "sm_120"
}

