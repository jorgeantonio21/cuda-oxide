fn test_gemm_32x32x32_runner(
    ctx: &Arc<CudaContext>,
    module: &kernels::LoadedModule,
    passed: &mut i32,
    failed: &mut i32,
) {
    println!("--- Test: test_gemm_32x32x32 ---");
    println!("    Testing 32x32x32 block-level GEMM: C = A * B");

    let m = 32usize;
    let n = 32usize;
    let k = 32usize;

    // Initialize matrices
    // A: 32x32 row-major (identity-ish for easy verification)
    // B: 32x32 col-major (identity-ish)
    let mut h_a = vec![0.0f32; m * k];
    let mut h_b = vec![0.0f32; k * n];

    // Make A an identity matrix (row-major)
    for i in 0..m.min(k) {
        h_a[i * k + i] = 1.0;
    }

    // Make B an identity matrix (col-major: B[i,j] at index j*k + i)
    for i in 0..k.min(n) {
        h_b[i * k + i] = 1.0;
    }

    let stream = ctx.default_stream();
    let d_a = DeviceBuffer::from_host(&stream, &h_a).unwrap();
    let d_b = DeviceBuffer::from_host(&stream, &h_b).unwrap();
    let d_c = DeviceBuffer::<f32>::zeroed(&stream, m * n).unwrap();

    let smem_bytes = 16 * 1024u32;

    let config = LaunchConfig {
        grid_dim: (1, 1, 1),
        block_dim: (256, 1, 1),
        shared_mem_bytes: smem_bytes,
    };

    let a_ptr = d_a.cu_deviceptr() as *const f32;
    let b_ptr = d_b.cu_deviceptr() as *const f32;
    let c_ptr = d_c.cu_deviceptr() as *mut f32;
    module
        .test_gemm_32x32x32((stream).as_ref(), config, a_ptr, b_ptr, c_ptr)
        .expect("Kernel launch failed");

    let h_c = d_c.to_host_vec(&stream).unwrap();

    // C = I * I = I, so C should be identity matrix
    let mut max_error = 0.0f32;
    for i in 0..m {
        for j in 0..n {
            let expected = if i == j { 1.0 } else { 0.0 };
            let actual = h_c[i * n + j];
            let error = (actual - expected).abs();
            if error > max_error {
                max_error = error;
            }
        }
    }

    let tolerance = 1e-3;
    if max_error < tolerance {
        println!("    ✓ PASSED (max_error={:.6})", max_error);
        *passed += 1;
    } else {
        println!("    ✗ FAILED (max_error={:.6})", max_error);
        // Print a few values for debugging
        println!(
            "    C[0,0]={}, C[1,1]={}, C[0,1]={}",
            h_c[0], h_c[33], h_c[1]
        );
        *failed += 1;
    }
}

fn test_gemm_32x32x32_alphabeta_runner(
    ctx: &Arc<CudaContext>,
    module: &kernels::LoadedModule,
    passed: &mut i32,
    failed: &mut i32,
) {
    println!("--- Test: test_gemm_32x32x32_alphabeta ---");
    println!("    Testing GEMM: C = 2.0 * A * B + 1.0 * C");

    let m = 32usize;
    let n = 32usize;
    let k = 32usize;

    // A = I, B = I (identity matrices)
    let mut h_a = vec![0.0f32; m * k];
    let mut h_b = vec![0.0f32; k * n];
    let mut h_c = vec![0.0f32; m * n];

    for i in 0..m.min(k) {
        h_a[i * k + i] = 1.0;
    }
    for i in 0..k.min(n) {
        h_b[i * k + i] = 1.0;
    }
    // Initialize C with 1.0 on diagonal
    for i in 0..m.min(n) {
        h_c[i * n + i] = 1.0;
    }

    let stream = ctx.default_stream();
    let d_a = DeviceBuffer::from_host(&stream, &h_a).unwrap();
    let d_b = DeviceBuffer::from_host(&stream, &h_b).unwrap();
    let d_c = DeviceBuffer::from_host(&stream, &h_c).unwrap();

    let smem_bytes = 16 * 1024u32;

    let config = LaunchConfig {
        grid_dim: (1, 1, 1),
        block_dim: (256, 1, 1),
        shared_mem_bytes: smem_bytes,
    };

    let alpha = 2.0f32;
    let beta = 1.0f32;

    let a_ptr = d_a.cu_deviceptr() as *const f32;
    let b_ptr = d_b.cu_deviceptr() as *const f32;
    let c_ptr = d_c.cu_deviceptr() as *mut f32;
    module
        .test_gemm_32x32x32_alphabeta((stream).as_ref(), config, alpha, a_ptr, b_ptr, beta, c_ptr)
        .expect("Kernel launch failed");

    let h_result = d_c.to_host_vec(&stream).unwrap();

    // C = 2*I*I + 1*I = 2*I + I = 3*I
    // Diagonal should be 3.0, off-diagonal should be 0.0
    let mut max_error = 0.0f32;
    for i in 0..m {
        for j in 0..n {
            let expected = if i == j { 3.0 } else { 0.0 };
            let actual = h_result[i * n + j];
            let error = (actual - expected).abs();
            if error > max_error {
                max_error = error;
            }
        }
    }

    let tolerance = 1e-3;
    if max_error < tolerance {
        println!("    ✓ PASSED (max_error={:.6})", max_error);
        *passed += 1;
    } else {
        println!("    ✗ FAILED (max_error={:.6})", max_error);
        println!("    C[0,0]={} (expected 3.0)", h_result[0]);
        *failed += 1;
    }
}

