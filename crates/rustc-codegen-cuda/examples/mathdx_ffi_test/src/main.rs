fn debug_copy_through_local_runner(
    ctx: &Arc<CudaContext>,
    module: &kernels::LoadedModule,
    passed: &mut i32,
    failed: &mut i32,
) {
    println!("--- Test: debug_copy_through_local ---");
    println!("    Testing as_mut_ptr() on local array (doubles values)");

    let num_threads = 4usize;
    let floats_per_thread = 16usize;
    let total_floats = num_threads * floats_per_thread;

    // Generate test input: simple pattern
    let h_input: Vec<f32> = (0..total_floats)
        .map(|i| (i % floats_per_thread) as f32)
        .collect();

    let stream = ctx.default_stream();
    let d_input = DeviceBuffer::from_host(&stream, &h_input).unwrap();
    let d_output = DeviceBuffer::<f32>::zeroed(&stream, total_floats).unwrap();

    let config = LaunchConfig {
        grid_dim: (1, 1, 1),
        block_dim: (num_threads as u32, 1, 1),
        shared_mem_bytes: 0,
    };

    let input_ptr = d_input.cu_deviceptr() as *const f32;
    let output_ptr = d_output.cu_deviceptr() as *mut f32;
    module
        .debug_copy_through_local((stream).as_ref(), config, input_ptr, output_ptr)
        .expect("Kernel launch failed");

    let h_output = d_output.to_host_vec(&stream).unwrap();

    // Output should be input * 2 (kernel doubles each value via as_mut_ptr)
    let mut max_error = 0.0f32;
    for i in 0..total_floats {
        let expected = h_input[i] * 2.0;
        let error = (h_output[i] - expected).abs();
        if error > max_error {
            max_error = error;
        }
    }

    let tolerance = 1e-6;
    if max_error < tolerance {
        println!("    ✓ PASSED (max_error={:.6})", max_error);
        *passed += 1;
    } else {
        println!("    ✗ FAILED (max_error={:.6})", max_error);
        println!(
            "    First few values: input={:?}, output={:?}",
            &h_input[0..4],
            &h_output[0..4]
        );
        println!(
            "    Expected (input*2): {:?}",
            h_input[0..4].iter().map(|x| x * 2.0).collect::<Vec<_>>()
        );
        *failed += 1;
    }
}

