    // 32x32x32 GEMM configuration queries
    fn cublasdx_gemm_32x32x32_smem_size() -> i32;
    fn cublasdx_gemm_32x32x32_smem_size_ab() -> i32;
    fn cublasdx_gemm_32x32x32_block_dim_x() -> i32;
    fn cublasdx_gemm_32x32x32_block_dim_y() -> i32;
